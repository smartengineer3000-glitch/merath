import React, { useEffect, useState, useRef } from 'react';
import { View, Text, ScrollView, TouchableOpacity, Animated, Switch, Alert, Platform } from 'react-native';
import { useCalc } from '../lib/context/CalcContext';
import { calculateInheritance } from '../lib/engine/calculator';
import { useAppTheme } from '../hooks/useAppTheme';
import { ExportBar } from '../components/ExportBar';
import { ResultsSkeleton } from '../components/SkeletonCard';
import { Button } from '../components/ui/Button';
import { PieChart } from '../components/PieChart';
import * as Print from 'expo-print';
import * as Sharing from 'expo-sharing';
import { saveAuditTrail } from '../lib/services/AuditTrailService';
import { t } from '../lib/i18n';

const AnimatedNumber = ({ value, style }: { value: number; style?: any }) => {
  const animatedValue = useRef(new Animated.Value(0)).current;
  useEffect(() => {
    Animated.timing(animatedValue, {
      toValue: value,
      duration: 1000,
      useNativeDriver: false,
    }).start();
  }, [value]);
  const display = animatedValue.interpolate({
    inputRange: [0, value],
    outputRange: [0, value],
    extrapolate: 'clamp',
  });
  return <Animated.Text style={style}>{display}</Animated.Text>;
};

const generatePDF = async (result: any) => {
  const html = '<html><head><style>body{font-family:\'Arial\';padding:20px} h1{color:#1B6B4A}</style></head><body><h1>Inheritance Report</h1><p>Madhab: ' + result.madhab + '</p><p>Net Estate: $' + result.netEstate + '</p><h2>Distribution</h2><ul>' + result.shares.map((s: any) => '<li>' + s.name + ': $' + s.amount.toFixed(2) + ' (' + s.fraction.numerator + '/' + s.fraction.denominator + ')</li>').join('') + '</ul><p><img src="data:image/png;base64,' + (result.chartImage || '') + '" style="max-width:100%;"/></p><footer>Generated by Merath App - ' + new Date().toLocaleDateString() + '</footer></body></html>';
  const { uri } = await Print.printToFileAsync({ html });
  if (Platform.OS === 'android') {
    await Sharing.shareAsync(uri);
  } else {
    Alert.alert('PDF Saved', 'Report saved to ' + uri);
  }
};

export const Results = ({ navigation }: any) => {
  const { state } = useCalc();
  const theme = useAppTheme();
  const [result, setResult] = useState<any>(null);
  const [loading, setLoading] = useState(true);
  const [showPercentage, setShowPercentage] = useState(false);

  useEffect(() => {
    const timer = setTimeout(() => {
      const estate = { total: state.total, funeral: state.funeral, debts: state.debts, will: state.will };
      const res = calculateInheritance(state.madhab, estate, state.heirs);
      let confidence = 100;
      if ((res.netEstate ?? 0) <= 0) confidence -= 50;
      if (state.heirs.length === 0) confidence -= 30;
      res.confidence = Math.max(confidence, 10);
      const chartData = res.shares.map((s: any) => ({
        label: s.name,
        value: s.amount,
        color: s.colour || '#1B6B4A',
      }));
      setResult({ ...res, chartData });
      setLoading(false);
      saveAuditTrail({
        id: Date.now().toString(),
        timestamp: new Date().toISOString(),
        madhab: state.madhab,
        netTotal: (res.netEstate ?? 0),
        shares: res.shares,
        steps: res.steps,
      });
    }, 600);
    return () => clearTimeout(timer);
  }, []);

  const copyAsText = () => {
    let text = 'Net Estate: $' + result.netEstate + '\n\n';
    result.shares.forEach((s: any) => {
      text += s.name + ': $' + s.amount.toFixed(2) + ' (' + s.fraction.numerator + '/' + s.fraction.denominator + ')\n';
    });
    require('expo-clipboard').setStringAsync(text);
    Alert.alert('Copied', 'Results copied to clipboard');
  };

  const confidenceColor = result?.confidence >= 70 ? theme.colors?.success || '#2E7D32' : result?.confidence >= 40 ? theme.colors?.warning || '#E65100' : theme.colors?.error || '#BA1A1A';

  if (loading) return <ResultsSkeleton />;

  return (
    <ExportBar resultData={result}>
      <ScrollView contentContainerStyle={{ padding: theme.spacing?.lg || 24 }}>
        <View style={{ backgroundColor: theme.colors?.primary || '#1B6B4A', borderRadius: theme.radius?.lg || 16, padding: theme.spacing?.lg || 24, alignItems: 'center', marginBottom: theme.spacing?.lg || 24 }}>
          <Text style={{ color: theme.colors?.onPrimary || '#fff', fontSize: 24 }}>{t('netEstate')}</Text>
          <AnimatedNumber value={result.netEstate} style={{ color: theme.colors?.onPrimary || '#fff', fontSize: 48 }} />
        </View>

        <View style={{ flexDirection: 'row', alignItems: 'center', marginBottom: theme.spacing?.md || 16 }}>
          <View style={{ height: 10, flex: 1, backgroundColor: theme.colors?.surfaceVariant || '#F0F0F3', borderRadius: 5, overflow: 'hidden' }}>
            <View style={{ height: 10, width: (result.confidence + '%') as any, backgroundColor: confidenceColor, borderRadius: 5 }} />
          </View>
          <Text style={{ marginLeft: 8, color: confidenceColor, fontWeight: '600' }}>{result.confidence}%</Text>
        </View>
        <Text style={theme.typography?.caption || { fontSize: 12, lineHeight: 16 }}>{t('confidence')}</Text>

        <View style={{ flexDirection: 'row', justifyContent: 'flex-end', alignItems: 'center', marginVertical: 12 }}>
          <Text style={{ marginRight: 8 }}>{t('fractions')}</Text>
          <Switch value={showPercentage} onValueChange={setShowPercentage} />
          <Text style={{ marginLeft: 8 }}>{t('percentages')}</Text>
        </View>

        {result.chartData && <PieChart data={result.chartData} />}

        <Text style={theme.typography?.h2 || { fontSize: 24, lineHeight: 32 }}>{t('distribution')}</Text>
        {result.shares.map((share: any, idx: number) => {
          const displayAmount = showPercentage
            ? ((share.amount / result.netEstate) * 100).toFixed(1) + '%'
            : '$' + share.amount.toFixed(2);
          return (
            <View key={idx} style={{ flexDirection: 'row', justifyContent: 'space-between', paddingVertical: theme.spacing?.sm || 8, borderBottomWidth: 1, borderColor: theme.colors?.outline || '#79747E' }}>
              <Text style={theme.typography?.body || { fontSize: 16, lineHeight: 24 }}>{share.name} ({share.fraction.numerator}/{share.fraction.denominator})</Text>
              <Text style={theme.typography?.body || { fontSize: 16, lineHeight: 24 }}>{displayAmount}</Text>
            </View>
          );
        })}

        <Text style={[theme.typography?.h2 || { fontSize: 24, lineHeight: 32 }, { marginTop: 20 }]}>{t('steps')}</Text>
        {result.steps.map((step: any, idx: number) => (
          <View key={idx} style={{ paddingVertical: 4 }}>
            <Text style={{ fontWeight: '600' }}>{step.title}</Text>
            <Text style={{ fontSize: 12 }}>{step.description}</Text>
          </View>
        ))}

        <View style={{ flexDirection: 'row', justifyContent: 'space-around', marginTop: theme.spacing?.lg || 24 }}>
          <Button title={t('compare')} onPress={() => navigation.navigate('Comparison')} mode="outlined" />
          <Button title={t('history')} onPress={() => navigation.navigate('History')} mode="outlined" />
        </View>
        <View style={{ flexDirection: 'row', justifyContent: 'space-around', marginTop: theme.spacing?.sm || 8 }}>
          <Button title={t('settings')} onPress={() => navigation.navigate('Settings')} mode="outlined" />
          <Button title={t('copy')} onPress={copyAsText} mode="outlined" />
        </View>
        <View style={{ flexDirection: 'row', justifyContent: 'space-around', marginTop: theme.spacing?.sm || 8 }}>
          <Button title={t('pdf')} onPress={() => generatePDF({ ...result, madhab: state.madhab })} mode="outlined" />
          <Button title={t('share')} onPress={() => {
            const link = 'merath://setup?total=' + state.total + '&madhab=' + state.madhab;
            require('expo-clipboard').setStringAsync(link);
            Alert.alert(t('share'), t('deepLinkCopied'));
          }} mode="outlined" />
        </View>
      </ScrollView>
    </ExportBar>
  );
};